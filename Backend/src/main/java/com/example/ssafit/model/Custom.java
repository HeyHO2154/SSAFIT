package com.example.ssafit.model;

public class Custom {
	private String userId;
	private int hair;
	private int shirts;
	private int pants;
	private int shoes;
	private int hat;
	private int glasses;
	private int bag;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public int getHair() {
		return hair;
	}
	public void setHair(int hair) {
		this.hair = hair;
	}
	public int getShirts() {
		return shirts;
	}
	public void setShirts(int shirts) {
		this.shirts = shirts;
	}
	public int getPants() {
		return pants;
	}
	public void setPants(int pants) {
		this.pants = pants;
	}
	public int getShoes() {
		return shoes;
	}
	public void setShoes(int shoes) {
		this.shoes = shoes;
	}
	public int getHat() {
		return hat;
	}
	public void setHat(int hat) {
		this.hat = hat;
	}
	public int getGlasses() {
		return glasses;
	}
	public void setGlasses(int glasses) {
		this.glasses = glasses;
	}
	public int getBag() {
		return bag;
	}
	public void setBag(int bag) {
		this.bag = bag;
	}
}
